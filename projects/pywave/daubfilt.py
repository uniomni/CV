def low2hi(lp):
    """low2hi - high pass filter from low pass filter.

    hp = low2hi(lp)

    Input
      lp: Low pass filter, for example obtained from daubfilt.

    Output:
      hp: Corresponding high pass filter

    See also daubfilt.

    Ole Moller Nielsen, UNI-C, 1995
    Adapted from Matlab by OMN, Geosciences Australia 2003
    """

    from Numeric import zeros, Float
    
    D = len(lp)

    if D % 2 != 0:
        msg = 'Wavelet genus must be an even number, D=%d' %D
        raise msg
    
    hp = zeros(D, Float)

    sign=-1
    for k in range(D):
        hp[D-k-1] = sign*lp[k]
        sign = -sign;

    return hp

    
